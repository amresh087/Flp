var global=0;
function bigImg(x) {
	
	if(global==0){
	setmaximg(x);
	
	global++;
	}else if(global>0)
	{
		setminimg(x)
		global--;
	}
}


function setmaximg(x)
{
x.style.height = "auto";
x.style.width = "400px";
}

function setminimg(x)
{
x.style.height = "auto";
x.style.width = "100px";
}

